package classes;

public class Evaluation {
    private String id_eval;
    private String id_vis;
    private String id_rep;


    public Evaluation() {
    }


    public String getId_eval() {
        return this.id_eval;
    }

    public void setId_eval(String id_eval) {
        this.id_eval = id_eval;
    }

    public String getId_vis() {
        return this.id_vis;
    }

    public void setId_vis(String id_vis) {
        this.id_vis = id_vis;
    }

    public String getId_rep() {
        return this.id_rep;
    }

    public void setId_rep(String id_rep) {
        this.id_rep = id_rep;
    }

    
}

