package classes;


public class Indice{
    private String id_ind;
    private String label_ind;
    private String description_ind;
    private Integer points_ind;
    private String id_defi;



    public Indice() {
    }




    public String getId_ind() {
        return this.id_ind;
    }

    public void setId_ind(String id_ind) {
        this.id_ind = id_ind;
    }

    public String getLabel_ind() {
        return this.label_ind;
    }

    public void setLabel_ind(String label_ind) {
        this.label_ind = label_ind;
    }

    public String getDescription_ind() {
        return this.description_ind;
    }

    public void setDescription_ind(String description_ind) {
        this.description_ind = description_ind;
    }

    public Integer getPoints_ind() {
        return this.points_ind;
    }

    public void setPoints_ind(Integer points_ind) {
        this.points_ind = points_ind;
    }

    public String getId_defi() {
        return this.id_defi;
    }

    public void setId_defi(String id_defi) {
        this.id_defi = id_defi;
    }
    


}