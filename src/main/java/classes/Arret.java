package classes;


public class Arret {
    private String code;
    private String lib_arret;
    private String streetMap;


    public Arret() {
    }
    

    public String getCode() {
        return this.code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getLib_arret() {
        return this.lib_arret;
    }

    public void setLib_arret(String arret) {
        this.lib_arret = arret;
    }

    public String getStreetMap() {
        return this.streetMap;
    }

    public void setStreetMap(String streetMap) {
        this.streetMap = streetMap;
    }


}
